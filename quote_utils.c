static bool is_quote_char(char c)
{
	if (c == '\'' || c == '\"')
		return (true);
	return (false);
}

/* ************************************************************************** */
static bool is_pair(int n)
{
	int reste;

	reste = n % 2;
	if (reste == 0)
		return (true);
	return (false);	
}
