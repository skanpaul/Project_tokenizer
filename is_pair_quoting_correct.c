/* ************************************************************************** */
static bool is_pair(int n);

/* ************************************************************************** */
bool is_pair_quoting_correct(char *line)
{
	int i;	
	t_quote_info	qti;
	
	init_quote_info(&qti);
		
	i = 0;
	while (line[i] != '\0')
	{
		refresh_quote_info(&qti, line[i]);
		i++;
	}
	
	if (is_pair(qti.cnt_real_quote))
		return (true);
		
	return (false);
}

/* ************************************************************************** */static bool is_pair(int n)
{
	int reste;

	reste = n % 2;
	if (reste == 0)
		return (true);
	return (false);	
}
