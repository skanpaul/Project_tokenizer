bool is_line_with_correct_quote(char *line)
{
	int i;	
	t_quote_info	qti;
	
	init_quote_info(&qti);
		
	i = 0;
	while (line[i] != '\0')
	{
		refresh_quote_info(&qti, line[i]);
		i++;
	}
	
	if (is_good_number_of_realquote(&qti))
		return (true);
		
	return (false);
}

